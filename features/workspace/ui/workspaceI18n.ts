export function workspaceTranslate(locale: Locale, text: string) {
  return locale === "zh-TW" ? (zhTw[text] ?? text) : text;
}

export function useWorkspaceI18n() {
  const { locale, setLocale } = useI18n();
  const tx = useCallback((text: string) => workspaceTranslate(locale, text), [locale]);
  return { locale, setLocale, tx };
}
