const PitchLocaleOverrideContext = createContext<Locale | null>(null);

export function PitchLocaleOverride({ children, locale }: { children: ReactNode; locale?: Locale }) {
  return createElement(PitchLocaleOverrideContext.Provider, { value: locale ?? null }, children);
}

export function pitchTranslate(locale: Locale, text: string) {
  return locale === "zh-TW" ? (zhTw[text] ?? text) : text;
}

export function usePitchI18n() {
  const { locale: workspaceLocale } = useI18n();
  const localeOverride = useContext(PitchLocaleOverrideContext);
  const locale = localeOverride ?? workspaceLocale;
  const tx = useCallback((text: string) => pitchTranslate(locale, text), [locale]);
  return { locale, tx };
}
