export function getGoogleFontUrl(fontFamily: string): string {
  if (!fontFamily || fontFamily === "inherit" || fontFamily === "Default Font" || fontFamily === "Default") {
    return "";
  }
  
  // Format font name for Google Fonts API (e.g. "Noto Sans TC" -> "Noto+Sans+TC")
  const formattedName = fontFamily.replace(/ /g, "+");
  return `https://fonts.googleapis.com/css2?family=${formattedName}:wght@400;500;600;700;800;900&display=swap`;
}
